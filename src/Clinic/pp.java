    public pp(String name, int status, OPD1 opd) {
        initComponents();
        this.opd = opd;
        call.setBackground(Color.blue);
        call.setForeground(Color.white);

        if (status == 3) {
            call.setBackground(Color.red);
            call.setForeground(Color.white);
            call.setText("Recall");
        }
        name = name.replaceAll("\\s+", " ");//to delete double spaces in a tex
        String[] st = name.split(" ");
        waiting_pt.setText(st[0] + " " + st[1]);
        int lastIndex = st.length;
        stid = st[lastIndex - 1].substring(st[lastIndex - 1].indexOf('(') + 1, st[lastIndex - 1].indexOf(')'));
        waiting_pt_mrn.setText(stid.toUpperCase());
    }

