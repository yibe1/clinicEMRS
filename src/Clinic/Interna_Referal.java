    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        opd.setEnabled(true);
        if (reason.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please Write Reasons For Referal!");
            return;
        }
        String referalReason = "Refer to room " + room.getSelectedItem() + " for the reason of " + reason.getText();
        plan.setText(plan.getText() + "\n" + referalReason);
        System.out.println("here..........to send rf");
        try {
            if (opd.save.getText().equals("Update")) {
                opd.updateHistory(0);
            }else{
                opd.saveUpdate();
            }
        } catch (SQLException ex) {
            System.out.println(ex);
            Logger.getLogger(Interna_Referal.class.getName()).log(Level.SEVERE, null, ex);
        }
        System.out.println("come out of update........");
        Repo repo = new Repo();
        try {
            System.out.println("sending referal........");
            repo.sendReferal(id, userId, Integer.parseInt((String) room.getSelectedItem()), referalReason);
        } catch (SQLException ex) {
            Logger.getLogger(Interna_Referal.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("error: " + ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

