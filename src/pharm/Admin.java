    public void fill_table(){
          ArrayList<String> drugs = new Bridge().getRequests_for_confirmation();
        for (int i = 0; i < drugs.size(); i++) {
            Object[] row = drugs.get(i).split(",");
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.addRow(row);
        } 
    }

