    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      int row = table.getSelectedRow();
      if(row == -1){
          JOptionPane.showMessageDialog(null, "Select row first");
          return;
      }else{
          int response = JOptionPane.showConfirmDialog(rootPane, "Are you sure you want to confirm the transaction happened?");
          if(response == 0){
              new Bridge().confirm(Integer.parseInt(String.valueOf(table.getValueAt(row, 0))));
              fill_table();
          }else{
              return;
          }
      }
    }//GEN-LAST:event_jButton1ActionPerformed

