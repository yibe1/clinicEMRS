    private int getQty(int qty) {
          
       if(u4.isVisible()){
           int x1 = Integer.parseInt(ut3.getText());
           int x2 = Integer.parseInt(ut2.getText());
           int x3 = Integer.parseInt(ut1.getText());
           return x1 * x2 * x3 * qty;
       }else if(u3.isVisible()){
           int x1 = Integer.parseInt(ut2.getText());
           int x2 = Integer.parseInt(ut1.getText());
           return x1 * x2 * qty;
       }else if(u2.isVisible()){
           int x1 = Integer.parseInt(ut1.getText());
           return x1 * qty;
       }else{
           return qty;
       }
    }

    private double getUnitPrice(float price) {
          
       if(u4.isVisible()){
           int x1 = Integer.parseInt(ut3.getText());
           int x2 = Integer.parseInt(ut2.getText());
           int x3 = Integer.parseInt(ut1.getText());
           return price /x1 /x2 /x3;
       }else if(u3.isVisible()){
           int x1 = Integer.parseInt(ut2.getText());
           int x2 = Integer.parseInt(ut1.getText());
           return price /x1 /x2;
       }else if(u2.isVisible()){
           int x1 = Integer.parseInt(ut1.getText());
           return price /x1;
       }else{
           return price;
       }
    }

