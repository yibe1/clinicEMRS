    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Drug drug = new Drug();
        drug.setBasic_unit(getBasicUnit());
        drug.setBatch_number(batch_n.getText());
        drug.setBrand_name(brand.getText());
        drug.setDate("");
        drug.setExpiry_date(y.getSelectedItem()+"-"+m.getSelectedItem()+"-"+d.getSelectedItem());
        drug.setGeneric_name((String) gen_name.getSelectedItem());
        drug.setManufacturer(supliyer.getText());
        drug.setPrefered_unit((String) u1.getSelectedItem());
        drug.setQuantity(getQty(Integer.parseInt(qty.getText())));
        drug.setStrength(strength.getText()+u.getSelectedItem());
        drug.setTr_type("in");
        drug.setUnit_price(getUnitPrice(Float.parseFloat(unit_price.getText())));
        drug.setTotal_mod(0);
        drug.setCreated_by(usr);
        drug.setLast_mod_by("Not modified");
        drug.setLast_mod_date(null);
        
        RepoPharma repo = new RepoPharma();
        repo.register(drug);
    }//GEN-LAST:event_jButton1ActionPerformed

